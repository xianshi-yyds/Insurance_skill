#!/usr/bin/env bash
# recommend.sh — 专业保障建议引擎 (温暖可靠版)
set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

if [ $# -lt 1 ]; then
    echo "Usage: bash recommend.sh \"<DATA>\""
    exit 1
fi

# 解析数据
IFS='|' read -r TAG AGE GENDER JOB INCOME IS_SERIOUS IS_DESIGNATED U_HISTORY <<< "$(echo "$1" | tr -d '\r')"

echo -e "${BLUE}╔══════════════════════════════════════════════════════════╗${NC}"
echo -e "${BLUE}║           💖 您专属的家庭保障与风险规划建议              ║${NC}"
echo -e "${BLUE}╚══════════════════════════════════════════════════════════╝${NC}"

# --- 1. 医疗与健康守护 ---
echo -e "\n${YELLOW}🛡️ 医疗保障解析 (基于众民保2026专业底座)${NC}"
echo "----------------------------------------"

if [ "$IS_SERIOUS" = "true" ]; then
    echo -e "  📢 ${RED}专业告知${NC}：通过分析，您的既往史 (${U_HISTORY}) 属于条款明确除外的范围。"
    echo "  [真诚建议]：为了避免您后续理赔受阻，我们不建议投保此款产品。"
    echo "  [替代关怀]：您可以重点关注政府背景的‘惠民保’，虽然额度较低，但通常对既往症更为包容。"
else
    echo -e "  ✨ ${GREEN}推荐方案：众民保·中高端医疗险 2026 (尊享版)${NC}"
    
    echo "  [专业保障深度解析]："
    if [ "$IS_DESIGNATED" = "true" ]; then
        echo -e "    - ${YELLOW}包容性承诺${NC}：虽然您有 ${U_HISTORY}，但产品依然为您开启了投保通道。"
        echo "      新保虽有比例折算，但这种‘带病可投’的机制是为您建立长效防护的可靠基础。"
    else
        echo "    - 优质体况优待：鉴于您目前身体状况良好，可享受最高 100% 的足额赔付待遇。"
    fi
    
    # 职业与生活
    if [[ "$JOB" == *"职员"* || "$JOB" == *"老师"* || "$JOB" == *"程序员"* || "$JOB" == *"大厂"* ]]; then
        echo "    - 高效就医：考虑到您的职业节奏，我们锁定了‘特需/国际部’资源，确保您在繁忙中也能获得高效诊疗。"
    else
        echo "    - 职业守护：无论您的工作环境多么艰辛 (${JOB})，众民保都不设门槛，给您最坚实的后盾。"
    fi
    
    echo "  [为您打造的安心亮点]："
    echo "    - 重疾住院 0 免赔：在突发大病时，您无需承担起付线，所有保内费用由我们直接覆盖。"
    echo "    - 康复全周期关怀：支持 33 家顶级机构康复直付，帮您不仅是‘康复’，更是‘重拾生活’。"
fi

# --- 2. 长期家庭财务稳健性 ---
echo -e "\n${YELLOW}🕯️ 长期财务与重疾对冲建议 (基于策略规划)${NC}"
echo "----------------------------------------"

ANNUAL=$((INCOME * 12))
if [ $ANNUAL -ge 200000 ]; then
    echo -e "  💎 ${GREEN}方案建议：AIA 爱伴航计划 2 (家庭资产底座)${NC}"
    echo "  [规划逻辑]：利用美元复利对冲未来的医药费通胀。首10年为您额外提供 50% 的重疾保额赠送，"
    echo "            这是在您事业拼搏期最贴心的家庭屏障。"
elif [ $ANNUAL -ge 100000 ]; then
    echo -e "  💎 ${GREEN}方案建议：友邦环宇盈活计划 (阶梯式储备)${NC}"
    echo "  [规划逻辑]：短期5年集中投入，锁定长期稳健收益。您可以灵活提取，用于子女教育或未来的品质养老。"
else
    echo "  💡 贴心建议：当前阶段建议您优先守住‘健康防线’（配置医疗险），等财务更为充裕时，我们再开启长期资产规划。"
fi

echo -e "\n${BLUE}────────────────────────────────────────${NC}"
echo -e "  💝 ${BLUE}温馨提醒${NC}：3月是政策切换窗口，建议尽早完成观察期建立，早一份保障，多一份安心。"
echo -e "  注意：以上建议基于条款深度解析，旨在为您提供更可靠的决策参考。${NC}"
