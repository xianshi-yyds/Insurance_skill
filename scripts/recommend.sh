#!/usr/bin/env bash
# recommend.sh — 专业保险建议书 (企划书格式)
set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

if [ $# -lt 1 ]; then
    echo "Usage: Insurance_skill recommend \"<DATA>\""
    exit 1
fi

# 解析数据
IFS='|' read -r TAG AGE GENDER JOB INCOME IS_SERIOUS IS_DESIGNATED U_HISTORY <<< "$(echo "$1" | tr -d '\r')"

# --- 企划书封面 ---
echo -e "${BLUE}================================================================${NC}"
echo -e "${BLUE}           📄 保 障 规 划 建 议 书 (Corporate Proposal)          ${NC}"
echo -e "           生成时间: $(date '+%Y-%m-%d %H:%M') | 编号: OYS-$(date '+%s') "
echo -e "${BLUE}================================================================${NC}"

# --- 第一部分：画像背景 ---
echo -e "\n${CYAN}[ 01 / 背景摘要 ]${NC}"
echo -e "----------------------------------------------------------------"
echo -e "  受访对象：${AGE}岁 / ${GENDER}性  |  所属行业：${JOB}"
echo -e "  年化收入预期：¥$((INCOME * 12))  |  健康状态评级：$( [ "$IS_SERIOUS" = "true" ] && echo -e "${RED}非标体 (限制投保)${NC}" || echo -e "${GREEN}标准体 (优质人群)${NC}" )"

# --- 第二部分：风险缺口分析 ---
echo -e "\n${CYAN}[ 02 / 核心风险评估 ]${NC}"
echo -e "----------------------------------------------------------------"
if [[ "$JOB" == *"互联网"* || "$JOB" == *"程序员"* || "$JOB" == *"大厂"* ]]; then
    echo -e "  · ${YELLOW}职业压力风险${NC}：高强度节奏可能诱发急性或慢性疾病，对就医效率有极高要求。"
fi
echo -e "  · ${YELLOW}医疗通胀风险${NC}：随着医疗技术（如 CAR-T、质子重离子）的发展，传统社保额度存在缺口。"
if [ "$AGE" -ge 40 ]; then
    echo -e "  · ${YELLOW}慢病管理风险${NC}：40岁后为各类慢病高发期，需前置药品与康复资源。"
fi

# --- 第三部分：深度方案设计 ---
echo -e "\n${CYAN}[ 03 / 专属产品架构 ]${NC}"
echo -e "----------------------------------------------------------------"

# 医疗模块
if [ "$IS_SERIOUS" = "true" ]; then
    echo -e "  【方案 A：普惠守底】 惠民保 (拟议)"
    echo -e "     - 分析：鉴于既往病史 (${U_HISTORY}) 属于中高端除外范围，建议转投政府普惠类保险。"
else
    echo -e "  【方案 A：医疗屏障】 众民保·2026 尊享版 (特需/国际/VIP)"
    echo -e "     - 赔付额度：¥6,000,000 (年度上限)"
    echo -e "     - 关键特权：重疾 0 免赔、基因检测直付、33家顶级机构康复保障。"
    if [ "$IS_DESIGNATED" = "true" ]; then
        echo -e "     - 特别强调：针对您的既往症 (${U_HISTORY})，新保首年按比例折算，但确保买得到、能报销。"
    fi
fi

# 资产与重疾模块
echo -e ""
ANNUAL=$((INCOME * 12))
if [ $ANNUAL -ge 200000 ]; then
    echo -e "  【方案 B：资产锚盘】 AIA 爱伴航计划 2 (美元复利杠杆)"
    echo -e "     - 核心功能：重疾保障 + 复利增值。首10年额外赠送 50% 保额。"
    echo -e "     - 精算理由：在事业巅峰期建立不可被侵蚀的离岸资产底座，对冲长周期重疾风险。"
elif [ $ANNUAL -ge 100000 ]; then
    echo -e "  【方案 B：灵活储备】 友邦环宇盈活计划 (阶梯式提取)"
    echo -e "     - 核心功能：5年集中投入，锁定终身收益。解决未来子女教育或高品质养老现金流。"
else
    echo -e "  【阶段建议】 筑基阶段"
    echo -e "     - 分析：当前建议优先固化【方案 A】的医疗防线，后续随收入增长增配定期寿险或储蓄计划。"
fi

# --- 第四部分：专业咨询结论 ---
echo -e "\n${CYAN}[ 04 / 专家测评与执行建议 ]${NC}"
echo -e "----------------------------------------------------------------"
echo -e "  💡 ${BLUE}规划师视角${NC}：您的这套组合体现了‘高频风险直付 + 长期风险对冲’的闭环思维。"
echo -e "  📅 ${BLUE}行动建议${NC}：利用 2026/3/31 前的政策窗口，建立观察期。这是对健康的守护，也是对财务的尊重。"

echo -e "\n${BLUE}================================================================${NC}"
echo -e "           声明：本建议书基于 PDF 知识库解析，最终以合同文本为准。"
echo -e "${BLUE}================================================================${NC}"
